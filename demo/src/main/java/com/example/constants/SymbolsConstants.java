package com.example.constants;

/**
 * This class defines a set of commonly used symbol constants.
 * These constants are useful for avoiding hardcoded strings and improving code readability.
 */
public class SymbolsConstants {

    /**
     * Represents an opening and closing brace pair: "{}".
     */
    public static final String OPENING_AND_CLOSING_BRACE = "{}";

    /**
     * Represents an opening brace: "{".
     */
    public static final String OPENING_BRACE = "{";

    /**
     * Represents a semicolon: ";".
     */
    public static final String SEMICOLON = ";";

    /**
     * Represents a plus sign: "+".
     */
    public static final String PLUS_SIGN = "+";

    /**
     * Represents a minus sign: "-".
     */
    public static final String MINUS_SIGN = "-";
}